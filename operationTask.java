package com.sag.sum.Calculator;


public class operationTask {


    public  int add(int x, int y)
    {
        x = x + y;
        return x;
    }
    public int sub(int x, int y)
    {
        x = x - y;
        return x;
    }
    public int mul(int x, int y)
    {
        x   = x * y;
        return x;
    }
    public  int div(int x, int y)
    {
        x = x / y;
        return x;
    }


}
